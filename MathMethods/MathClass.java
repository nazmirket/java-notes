package MathMethods;

public class MathClass {

	public static void main(String[] args) {
		
		
	 //BASIC METHODS
		
		Math.toRadians(34);// degree to radian
		
		Math.abs(-10); // this gives absolute value 
		
		Math.ceil(2.3); // rounds up the number      2.3 => 3
		
		Math.floor(2.1); // rounds down the number     2.1 => 2
		
		Math.round(2.4); // returns the closest integer
		
		Math.max(3, 5); // returns greater one
		
		Math.min(4, 1); // returns smaller one
		
		Math.pow(2, 3); // returns  2^3
		
		Math.sqrt(16); // 16^(1/2)
		
		Math.random(); // returns a value on the [0,1] interval randomly
		
	    
	  // TRIGONOMETRIC VALUES
		
		 Math.sin(Math.PI); // returns the sin value  sin(180) = 0  <pi radian = 90 degree> 
		
		 Math.sin(Math.toRadians(90)); // if we use degree instead of the radian
		 
		 Math.hypot(3, 4); // to use the Phisagor Theorem     3 , 4 => 5
		 
		 
	    // LOGARITHM
		 
		 Math.log(Math.E); // returns ln(e)    ln(e) = 1
		 
		 Math.log10(10);  // returns log(10)     log(10) = 1
		 
    		
    		
    		   
    		   
    	   }
    	   
       
	
	
	
    	   
    	   
       }
		
		
		
		
		
		
		
		
		
		
	


