package Loops;

public class DoWhileLoop {
	
	
	
	public static void main(String[] args) {
		
		
		int a = 0;
		
		
		//Here is a an example;
		do {
			
			a = a+1;
			
			System.out.println(a);
			
			a++;
			
			
		}while(a<10);
		
		/*
		 
		 do{
		 
		 Statements;
		 
		 increment or decrement;
		 
		 }while(conditions);
		 
		 */
		
		//Here there is a little difference, this loop works one time and checks the conditions.
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
