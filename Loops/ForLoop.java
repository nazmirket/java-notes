package Loops;

public class ForLoop {
	
	
	public static void main(String[] args) {
		
		
		int a= 0;
		
	//Here is a for loop example;
	for(int i=0;i<10;i++) {
		
		a += i;
	}
	
	System.out.println(a);
	
	/*
	 Syntax should be like this;
	 
	 
	 for (number = initial;condition;increment or decrement;){
	 
	   statements
	 }
	 
	 
	 
	 */
	
	
	
	
	}

}
